img=im2double(imread('lena.bmp'));
img0=imnoise(img,'gaussian',0.5,0.01);
img1=medfilt2(img0);
p=[1,1,1;1,1,1;1,1,1]/9;
img2=conv2(img0,p);
H=fspecial('gaussian',size(img0)/4,0.1);
img3=imfilter(img0,H,'replicate');
subplot(2,2,1)
imshow(img0)
title('加噪声图像')
subplot(2,2,2)
imshow(img1)
title('中值滤波')
subplot(2,2,3)
imshow(img2)
title('均值滤波')
subplot(2,2,4)
imshow(img3)        
title('高斯滤波')
saveas(gcf,'t1.jpg')
